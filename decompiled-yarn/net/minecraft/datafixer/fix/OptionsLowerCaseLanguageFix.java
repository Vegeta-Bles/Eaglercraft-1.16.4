package net.minecraft.datafixer.fix;

import com.mojang.datafixers.DSL;
import com.mojang.datafixers.DataFix;
import com.mojang.datafixers.TypeRewriteRule;
import com.mojang.datafixers.schemas.Schema;
import java.util.Locale;
import java.util.Optional;
import net.minecraft.datafixer.TypeReferences;

public class OptionsLowerCaseLanguageFix extends DataFix {
   public OptionsLowerCaseLanguageFix(Schema outputSchema, boolean changesType) {
      super(outputSchema, changesType);
   }

   public TypeRewriteRule makeRule() {
      return this.fixTypeEverywhereTyped(
         "OptionsLowerCaseLanguageFix", this.getInputSchema().getType(TypeReferences.OPTIONS), _snowman -> _snowman.update(DSL.remainderFinder(), _snowmanx -> {
               Optional<String> _snowmanx = _snowmanx.get("lang").asString().result();
               return _snowmanx.isPresent() ? _snowmanx.set("lang", _snowmanx.createString(_snowmanx.get().toLowerCase(Locale.ROOT))) : _snowmanx;
            })
      );
   }
}
