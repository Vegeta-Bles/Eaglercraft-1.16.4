package net.minecraft.world.gen.feature.size;

import com.mojang.serialization.Codec;
import com.mojang.serialization.codecs.RecordCodecBuilder;
import java.util.OptionalInt;

public class ThreeLayersFeatureSize extends FeatureSize {
   public static final Codec<ThreeLayersFeatureSize> CODEC = RecordCodecBuilder.create(
      _snowman -> _snowman.group(
               Codec.intRange(0, 80).fieldOf("limit").orElse(1).forGetter(_snowmanx -> _snowmanx.limit),
               Codec.intRange(0, 80).fieldOf("upper_limit").orElse(1).forGetter(_snowmanx -> _snowmanx.upperLimit),
               Codec.intRange(0, 16).fieldOf("lower_size").orElse(0).forGetter(_snowmanx -> _snowmanx.lowerSize),
               Codec.intRange(0, 16).fieldOf("middle_size").orElse(1).forGetter(_snowmanx -> _snowmanx.middleSize),
               Codec.intRange(0, 16).fieldOf("upper_size").orElse(1).forGetter(_snowmanx -> _snowmanx.upperSize),
               createCodecBuilder()
            )
            .apply(_snowman, ThreeLayersFeatureSize::new)
   );
   private final int limit;
   private final int upperLimit;
   private final int lowerSize;
   private final int middleSize;
   private final int upperSize;

   public ThreeLayersFeatureSize(int limit, int upperLimit, int lowerSize, int middleSize, int upperSize, OptionalInt minClippedHeight) {
      super(minClippedHeight);
      this.limit = limit;
      this.upperLimit = upperLimit;
      this.lowerSize = lowerSize;
      this.middleSize = middleSize;
      this.upperSize = upperSize;
   }

   @Override
   protected FeatureSizeType<?> getType() {
      return FeatureSizeType.THREE_LAYERS_FEATURE_SIZE;
   }

   @Override
   public int method_27378(int _snowman, int _snowman) {
      if (_snowman < this.limit) {
         return this.lowerSize;
      } else {
         return _snowman >= _snowman - this.upperLimit ? this.upperSize : this.middleSize;
      }
   }
}
