package net.minecraft.client.util;

import com.mojang.text2speech.Narrator;
import java.util.UUID;
import net.fabricmc.api.EnvType;
import net.fabricmc.api.Environment;
import net.minecraft.SharedConstants;
import net.minecraft.client.MinecraftClient;
import net.minecraft.client.gui.ClientChatListener;
import net.minecraft.client.options.NarratorMode;
import net.minecraft.client.toast.SystemToast;
import net.minecraft.client.toast.ToastManager;
import net.minecraft.network.MessageType;
import net.minecraft.text.LiteralText;
import net.minecraft.text.Text;
import net.minecraft.text.TranslatableText;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;

@Environment(EnvType.CLIENT)
public class NarratorManager implements ClientChatListener {
   public static final Text EMPTY = LiteralText.EMPTY;
   private static final Logger LOGGER = LogManager.getLogger();
   public static final NarratorManager INSTANCE = new NarratorManager();
   private final Narrator narrator = Narrator.getNarrator();

   public NarratorManager() {
   }

   @Override
   public void onChatMessage(MessageType messageType, Text message, UUID senderUuid) {
      NarratorMode lv = getNarratorOption();
      if (lv != NarratorMode.OFF && this.narrator.active()) {
         if (lv == NarratorMode.ALL
            || lv == NarratorMode.CHAT && messageType == MessageType.CHAT
            || lv == NarratorMode.SYSTEM && messageType == MessageType.SYSTEM) {
            Text lv2;
            if (message instanceof TranslatableText && "chat.type.text".equals(((TranslatableText)message).getKey())) {
               lv2 = new TranslatableText("chat.type.text.narrate", ((TranslatableText)message).getArgs());
            } else {
               lv2 = message;
            }

            this.narrate(messageType.interruptsNarration(), lv2.getString());
         }
      }
   }

   public void narrate(String text) {
      NarratorMode lv = getNarratorOption();
      if (this.narrator.active() && lv != NarratorMode.OFF && lv != NarratorMode.CHAT && !text.isEmpty()) {
         this.narrator.clear();
         this.narrate(true, text);
      }
   }

   private static NarratorMode getNarratorOption() {
      return MinecraftClient.getInstance().options.narrator;
   }

   private void narrate(boolean interrupt, String message) {
      if (SharedConstants.isDevelopment) {
         LOGGER.debug("Narrating: {}", message.replaceAll("\n", "\\\\n"));
      }

      this.narrator.say(message, interrupt);
   }

   public void addToast(NarratorMode option) {
      this.clear();
      this.narrator.say(new TranslatableText("options.narrator").append(" : ").append(option.getName()).getString(), true);
      ToastManager lv = MinecraftClient.getInstance().getToastManager();
      if (this.narrator.active()) {
         if (option == NarratorMode.OFF) {
            SystemToast.show(lv, SystemToast.Type.NARRATOR_TOGGLE, new TranslatableText("narrator.toast.disabled"), null);
         } else {
            SystemToast.show(lv, SystemToast.Type.NARRATOR_TOGGLE, new TranslatableText("narrator.toast.enabled"), option.getName());
         }
      } else {
         SystemToast.show(
            lv, SystemToast.Type.NARRATOR_TOGGLE, new TranslatableText("narrator.toast.disabled"), new TranslatableText("options.narrator.notavailable")
         );
      }
   }

   public boolean isActive() {
      return this.narrator.active();
   }

   public void clear() {
      if (getNarratorOption() != NarratorMode.OFF && this.narrator.active()) {
         this.narrator.clear();
      }
   }

   public void destroy() {
      this.narrator.destroy();
   }
}
